import Foundation
import UIKit

extension UIColor {
    
    class func lightBlue() -> UIColor {
        return UIColor(red: 143/255.0, green: 70/255.0, blue: 121/255.0, alpha: 0.4);
    }
    
    class func blue() -> UIColor {
        return UIColor(red: 95/255.0, green: 12/255.0, blue: 90/255.0, alpha: 0.4);
    }
    
    class func darkBlue() -> UIColor {
        return UIColor(red: 6/255.0, green: 7/255.0, blue: 110/255.0, alpha: 0.4);
    }
    
}